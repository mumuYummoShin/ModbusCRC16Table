//modbus PDU 结构体
typedef struct tagModbusPDU
{
    uint8_t u8Func;
    uint8_t *pu8Data; //指向databuff的指针
    uint8_t u8DataLen; //数据域长度
}MbPDU_t;

//modbus ADU 结构体
typedef struct tagMdobusADU
{
    uint8_t  u8Addr;
    MbPDU_t  Pdu;
    uint16_t u16CRC; //MB CRC16 Lo在高位，Hi在低位
}MbADU_t;

